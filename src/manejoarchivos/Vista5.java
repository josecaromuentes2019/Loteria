    private void Generar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Generar1MouseClicked
        // TODO add your handling code here:
        
        Archivo objArchivo = new Archivo();
        String nombreLoteria = (String)Loterias.getSelectedItem();
        if(nombreLoteria.equals("Seleccione.")){
            JOptionPane.showMessageDialog(null, "Seleccione una Loteria.");
        }else{

        String nombre=(String)Loterias.getSelectedItem();
        String ruta;
        ruta = "C:\\BDLoterias\\" + nombre + ".txt";
        int nFilas = objArchivo.NumeroRegistros(ruta);
        
        if(bandera){
        valor = Integer.parseInt(JOptionPane.showInputDialog(null, "El archivo contiene " + nFilas + " Registros\nCon Cuantos registros\nDeseas Generar Numeros"));
        bandera=false;
        }
        System.out.println(valor);
        if(opcion<4){
            opcion++;
        }else{
            opcion=0;
        }
//            System.out.println(opcion);
        
         if(valor<=nFilas){

        switch(opcion){
            case 1:
                
//                nombre = (String)Loterias.getSelectedItem();
                ruta = "C:\\BDLoterias\\"+nombre+".txt";
                String ma [][]= objArchivo.leer(ruta, valor);
                objArchivo.generarNumero(ma,opcion);
                break;
                
            case 2:
        
//                nombre = (String)Loterias.getSelectedItem();
                ruta = "C:\\BDLoterias\\"+nombre+".txt";
                String ma1 [][]= objArchivo.leer(ruta, valor);
                objArchivo.generarNumero(ma1,opcion);
                break;
            case 3:
                
//                nombre = (String)Loterias.getSelectedItem();
                ruta = "C:\\BDLoterias\\"+nombre+".txt";
                String ma2 [][]= objArchivo.leer(ruta, valor);
                objArchivo.generarNumero(ma2,opcion);
                break;
            case 4:
                
//                nombre = (String)Loterias.getSelectedItem();
                ruta = "C:\\BDLoterias\\"+nombre+".txt";
                String ma3 [][]= objArchivo.leer(ruta, valor);
                objArchivo.generarNumero(ma3,opcion);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Solo es posible Generar 4 numeros");
       
        }
         }else{
             JOptionPane.showMessageDialog(null, "Digite un numero menor a "+nFilas);
              valor = Integer.parseInt(JOptionPane.showInputDialog(null, "El archivo contiene " + nFilas + " Registros\nCon Cuantos registros\nDeseas Generar Numeros"));
         }
        }
    }//GEN-LAST:event_Generar1MouseClicked

